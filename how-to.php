					<p class="HowTo-slideDescription">By otrzymać dostęp do zasobów płatnych, musisz przejść przez proces dokonywania płatności. Pierwszy krok to dodanie zasobu do koszyka.
Możesz też wybrać zasób w ramach posiadanego abonamentu. Wwystarczy, że klikniesz przycisk „Wybierz w ramach abonamentu”.
Zasoby z kategorii Knowledge to Share, Knowledge to Master i Knowledge to Listen możesz również podarować innym użytkownikom jako prezent (dotyczy użytkowników z przyznanym dostępem do Panelu Networkingowego). Służy do tego ikonka prezentu, umieszczona pod obrazkiem. Dodane zasoby automatycznie zostają przeniesione do koszyka (ikonka w prawym górnym rogu). Przycisk umieszczony w dolnym prawym rogu okienka przeniesie Cię do obszaru finalizacji zamówienia.
W tym celu musisz uzupełnić dane niezbędne do wystawienia faktury. Kolejny krok to dokonanie płatności przez system PayU – postępuj zgodnie z jego instrukcjami. My powiadomimy Cię, gdy tylko Twoja płatność zostanie zaksięgowana i będziesz mógł pobrać zasób.</p>
